#flag: crypto{biclique}

#explanation:
#A biclique attack is a variant of the meet-in-the-middle (MITM) method of cryptanalysis.
#It utilizes a biclique structure to extend the number of possibly attacked rounds by the MITM attack.
#Since biclique cryptanalysis is based on MITM attacks,
#it is applicable to both block ciphers and (iterated) hash-functions.
#Biclique attacks are known for having broken both full AES and full IDEA,
#though only with slight advantage over brute force.
#It has also been applied to the KASUMI cipher and preimage resistance of the Skein-512 and SHA-2 hash functions.